function drawPlatform(platform) {
    platforms = [];
    coins = [];
    swords = [];
    bronzes = [];
    for (let row = 0; row < platformHeight; row++) {
        for (let col = 0; col < platformWidth; col++) {
            if (platform[row][col] === 1) {
                const x = col * blockSize;
                const y = row * blockSize;
                let p = new Platform(x, y, blockSize, blockSize);
                platforms.push(p);
            }
            else if (platform[row][col] === 9) {
                const x = col * blockSize;
                const y = row * blockSize;
                let c = new Item(x, y);
                c.currentHeight = 20;
                c.currentWidth = 20;
                coins.push(c);
            }

            else if (platform[row][col] === 3) {
                const x = col * blockSize;
                const y = row * blockSize;
                let s = new Sword(x, y);
                s.height = 90;
                swords.push(s);
            }

            else if (platform[row][col] === 5) {
                const x = col * blockSize;
                const y = row * blockSize;
                let s = new Sword(x - 25, y - 30);
                s.height = 90;
                s.image = s.sprites.swordDown;
                swords.push(s);
            }

            else if (platform[row][col] === 4) {
                const x = col * blockSize;
                const y = row * blockSize;
                let s = new Sword(x - 25, y - 30);
                s.height = 90;
                s.image = s.sprites.swordRight;
                swords.push(s);
            }

            else if (platform[row][col] === 6) {
                const x = col * blockSize;
                const y = row * blockSize;
                let b = new Item(x + 10, y);
                b.currentOneImage = b.sprites.bronze.img;
                b.currentHeight = 40;
                b.currentWidth = 40;
                bronzes.push(b);

            }



        }
    }
}


