function IsCollided(x1, x2, y1, y2, w1, w2, h1, h2) {

    return x1 < x2 + w2 && x1 + w1 > x2 &&
        y1 < y2 + h2 && y1 + h1 > y2;

}


function removeElementFromArray(element, array) {
    let index = array.indexOf(element);

    if (index != -1)
        array.splice(index, 1);
}

